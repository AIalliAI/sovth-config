"""Self-evaluation for the high/max multi-pass research loop.

The LLM rates its own wiki output on four criteria, each 1-10:
- accuracy: claims match sources
- completeness: covers key facets of the source
- sourcing: citations present, reputable, traceable
- clarity: well-written, well-organized

If any score < 8, the runner requests another pass with the specific gaps.
The runner caps at max_passes to bound cost.
"""

from __future__ import annotations

from dataclasses import dataclass, asdict
from typing import Any


@dataclass
class QualityScore:
    accuracy: float
    completeness: float
    sourcing: float
    clarity: float

    @property
    def overall(self) -> float:
        return (self.accuracy + self.completeness + self.sourcing + self.clarity) / 4.0

    @property
    def min_score(self) -> float:
        return min(self.accuracy, self.completeness, self.sourcing, self.clarity)

    def gaps(self) -> list[str]:
        """Return a list of criteria below 8.0 with their current score."""
        out = []
        for name in ("accuracy", "completeness", "sourcing", "clarity"):
            score = getattr(self, name)
            if score < 8.0:
                out.append(f"{name}={score:.1f}")
        return out

    def to_dict(self) -> dict[str, Any]:
        d = asdict(self)
        d["overall"] = self.overall
        d["min"] = self.min_score
        return d


SELF_EVAL_PROMPT = """\
You are reviewing a wiki page generated by deep research. Rate the wiki
on each of these four criteria, 1-10:

1. **accuracy** — Do the claims match the cited sources? Are facts correct?
2. **completeness** — Does the wiki cover the key facets of the source?
3. **sourcing** — Are citations present, traceable, and reputable?
4. **clarity** — Is the writing well-organized and easy to read?

Output your rating as JSON only, no other prose:
{{"accuracy": <float>, "completeness": <float>, "sourcing": <float>, "clarity": <float>, "notes": "<short string>"}}

Be honest. A 10 is reserved for genuinely excellent work; a 7 is "good
enough to ship but could be better." Rate below 8 on any criterion that
has clear room for improvement — that signals the runner to do another pass.
"""


def parse_self_eval(llm_output: str) -> QualityScore:
    """Parse the LLM's JSON self-eval. Tolerates small format quirks."""
    import json
    import re

    # Extract the first {...} block from the output.
    match = re.search(r"\{[^{}]*\}", llm_output, re.DOTALL)
    if not match:
        # Default to a passing score if we can't parse; cap at 1 pass.
        return QualityScore(accuracy=7.0, completeness=7.0, sourcing=7.0, clarity=7.0)
    try:
        data = json.loads(match.group(0))
    except json.JSONDecodeError:
        return QualityScore(accuracy=7.0, completeness=7.0, sourcing=7.0, clarity=7.0)
    return QualityScore(
        accuracy=float(data.get("accuracy", 7.0)),
        completeness=float(data.get("completeness", 7.0)),
        sourcing=float(data.get("sourcing", 7.0)),
        clarity=float(data.get("clarity", 7.0)),
    )


def should_continue(score: QualityScore, threshold: float = 8.0) -> bool:
    """Decide whether to do another pass."""
    return score.min_score < threshold
